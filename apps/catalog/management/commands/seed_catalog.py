def make_product_image(name: str, palette_idx: int) -> ContentFile:
    color_top, color_bottom = PALETTES[palette_idx % len(PALETTES)]
    width, height = 800, 800
    img = Image.new('RGB', (width, height), color_bottom)
    draw = ImageDraw.Draw(img)

    for y in range(height):
        ratio = y / height
        r = int(color_top[0] * (1 - ratio) + color_bottom[0] * ratio)
        g = int(color_top[1] * (1 - ratio) + color_bottom[1] * ratio)
        b = int(color_top[2] * (1 - ratio) + color_bottom[2] * ratio)
        draw.line([(0, y), (width, y)], fill=(r, g, b))

    cx, cy = width // 2, height // 2
    for _ in range(7):
        px = cx + random.randint(-120, 120)
        py = cy + random.randint(-80, 80)
        pr = random.randint(40, 90)
        shade = tuple(max(0, c - random.randint(10, 40)) for c in color_top)
        draw.ellipse([px - pr, py - pr, px + pr, py + pr], fill=shade)

    try:
        font = ImageFont.truetype('arial.ttf', 28)
    except OSError:
        font = ImageFont.load_default()

    short_name = name if len(name) <= 22 else name[:20] + '…'
    bbox = draw.textbbox((0, 0), short_name, font=font)
    tw = bbox[2] - bbox[0]
    draw.text(((width - tw) // 2, height - 80), short_name, fill=(80, 70, 65), font=font)

    buffer = io.BytesIO()
    img.save(buffer, format='JPEG', quality=85)
    buffer.seek(0)
    filename = f'{name[:40].replace(" ", "-").lower()}.jpg'
    return ContentFile(buffer.read(), name=filename)


class Command(BaseCommand):
    help = 'Заполняет каталог категориями и товарами (букеты и композиции)'

    def add_arguments(self, parser):
        parser.add_argument(
            '--clear',
            action='store_true',
            help='Удалить существующие категории и товары перед заполнением',
        )

    def handle(self, *args, **options):
        if options['clear']:
            deleted_products, _ = Product.objects.all().delete()
            deleted_categories, _ = Category.objects.all().delete()
            self.stdout.write(f'Удалено: {deleted_products} товаров, {deleted_categories} категорий.')

        category_map = {}
        for cat_data in CATEGORIES:
            category, created = Category.objects.get_or_create(
                slug=cat_data['slug'],
                defaults={
                    'name': cat_data['name'],
                    'order': cat_data['order'],
                },
            )
            category_map[cat_data['slug']] = category
            action = 'Создана' if created else 'Найдена'
            self.stdout.write(f'{action} категория: {category.name}')

        for item in PRODUCTS:
            category = category_map[item['category']]
            product, created = Product.objects.get_or_create(
                slug=item['slug'],
                defaults={
                    'category': category,
                    'name': item['name'],
                    'description': item['description'],
                    'price': Decimal(item['price']),
                    'stock': item['stock'],
                    'available': True,
                },
            )
            if created or not product.image:
                product.image.save(
                    f"{item['slug']}.jpg",
                    make_product_image(item['name'], item['palette']),
                    save=True,
                )
            status = 'Создан' if created else 'Обновлён'
            stock_label = 'в наличии' if product.stock > 0 else 'под заказ'
            self.stdout.write(
                f'{status}: {product.name} - {product.price} rub. ({stock_label})'
            )

        self.stdout.write(self.style.SUCCESS(
            f'\nГотово: {Category.objects.count()} категорий, {Product.objects.count()} товаров.'
        ))
